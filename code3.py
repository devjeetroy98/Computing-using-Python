mylist = ['1', 10, True, 'Py', 10.23]
print(mylist)

# Indexing
print(mylist[2])

# Slicing
print(mylist[1:4])
print(mylist[1:5])
print(mylist[1:])
print(mylist[:3])
print(mylist[::2])
print(mylist[-1])

# Finding element count in list
print(len(mylist))

# 2D List
a = [1, 2, 3]
b = [3, 4, 5]
matrix = [a, b]
print(matrix)

# 2D Indexing
print(matrix[0][1])
# Slicing for 2D List
print(matrix[0][1:])
